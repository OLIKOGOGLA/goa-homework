# 11. შეინახეთ ორი რიცხვი ცალკეულ ცვლადებში და გამოიყენეთ შედარების ოპერატორები (`<`, `>`, `<=`, `>=`, `==`, `!=`) შედარებისთვის. დაპრინტეთ თითოეული შედარების შედეგი.

age = 15
age__ =17
print(age < age__)
print(age > age__)
print(age <= age__)
print(age__ >= age)
print(age__ == age)
print(age__ != age)

