#4. while loop-ის მეშვეობით გამოიტანეთ ციფრები 10-დან 30-მდე.

number = 10
while number <= 30:
    print(number)
    number += 1

#5. while loop-ის მეშვეობით გამოიტანეთ ციფრები 50-დან 10-მდე.

number = 50
while number >= 10:
    print(number)
    number -= 1

#6. while loop-ის მეშვეობით გამოიტანეთ 'goa best' 10-ჯერ.

count = 0
while count < 10:
    print('goa best')
    count += 1
