##1. შექმენით if-else, რომელიც ამოწმებს ტემპერატურის ცვლადი მეტია თუ არა 30-ზე, 
# თუ არის, გამოიტანოს "it's hot outside", თუ არ არის "it's not too hot outside".


temperature__=int(input("enter your temperature "))

temperature = 30

if temperature__ > temperature:
    print("it's hot outside")

if temperature__ < temperature:
    print("it's not too hot outside")

